package com.smartsparrow.util;

public class ImageDimensions {

    Double width;
    Double height;

    public Double getWidth() {
        return width;
    }

    public ImageDimensions setWidth(Double width) {
        this.width = width;
        return this;
    }

    public Double getHeight() {
        return height;
    }

    public ImageDimensions setHeight(Double height) {
        this.height = height;
        return this;
    }
}
